    private void FecharFrame() {
        this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        _crnEtapaReiniciar.Parar();
        _crnEtapaReiniciar.removerOuvinte(this);
        MoveMente.Comandos.RemoverOuvinte(this);
        this.hide();
        this.dispose();
    }

